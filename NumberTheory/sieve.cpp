#define sz 100000010
bool siv[sz];
vector<long long >primes;
void seive(){
    long long  limit = sqrt(sz+1);
    for (long long  i=3; i<=limit; i+=2)
        if(!siv[i])
            for (long  j = i*i; j<sz; j+=i+i)
                siv[j] = 1;
    primes.push_back(2);
    for (long long i=3; i<sz; i+=2)
        if(!siv[i]) primes.push_back(i);
}